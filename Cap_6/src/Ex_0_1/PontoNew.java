package Ex_0_1;

public class PontoNew {
	private int x;
	private int y;
	
	//construtor

	public PontoNew() {
		
	}
	
	public PontoNew(int x, int y) {
		this.x = x;
		this.y = y;
	}
	
	public void setX (int x) {
		this.x = x;
	}
	
	public void setY (int y) {
		this.y = y;
	}
	
	public int getX() {
		return x;
	}
	
	public int getY() {
		return y;
	}
	
	
}

