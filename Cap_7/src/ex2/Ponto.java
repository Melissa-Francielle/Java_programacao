package ex2;

public class Ponto {
	private int x, y;
	
	public Ponto() {}
	
	public Ponto(int x, int y) {
		x = 0; 
		y = 0;
	}
	
	public int getX() {
		return x;
	}
	
	public void setX(int x) {
		this.x = x;
	}
	
	public int getY() {
		return y;
	}
	
	public void setY(int y) {
		this.y =y;
	}
}
